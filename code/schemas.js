"use strict";
const _ajv = require("ajv");

const ajv = new _ajv({
  allErrors: true,
  removeAdditional: "all",
  useDefaults: true
});

function define(jsonSchema) {
  const fn = ajv.compile(jsonSchema);
  return Object.assign({}, jsonSchema, {
    check(input) {
      const valid = fn(input);
      if (!valid) {
        return {
          // OMG sticking errors on the ajv instance is such a terrible API!
          message:
            "Invalid input:\n" + // eslint-disable-line prefer-template
            fn.errors.map(e => `${e.dataPath} ${e.message}`).join("\n")
        };
      }
      return null;
    },
    openapi() {
      const api = Object.assign({}, jsonSchema);
      if (typeof jsonSchema.example === "function") {
        api.example = jsonSchema.example();
      }
      return api;
    }
  });
}

exports.define = define;

